int	check_p_path_valid_or_not(char **p)
{
	int	x;
	int	y;

	y = -1;
	x = -1;
	while (p[++y])
	{
		while (p[y][++x])
		{
			if (p[y][x] == 'C' || p[y][x] == 'E')
				return (0);
		}
		x = -1;
	}
	return (1);
}
